# Unary  Plus (+):
# This operator doesn't really do anything. It's more for clarity and symmetry in your code.
#Ex:
a = 5
print(+a)  # Prints: +5

#Unary Minus (-):
# This operator negates the value of the operand.
#Ex:
a = 5
print(-a)  # Prints: -5

# Logical Negation (not):
#Ex:
a = True
print(not a)  # Prints: False


